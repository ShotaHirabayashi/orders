package com.exaple.orders.form;

public class ViewForm {

	
	
	private int id;
	private String productsName;
	private int productsPriceFrom;
	private int productsPriceTo;
	private String customerName;
	private String companyName;
	private int sortDate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProductsName() {
		return productsName;
	}
	public void setProductsName(String productsName) {
		this.productsName = productsName;
	}
	public int getProductsPriceFrom() {
		return productsPriceFrom;
	}
	public void setProductsPriceFrom(int productsPriceFrom) {
		this.productsPriceFrom = productsPriceFrom;
	}
	public int getProductsPriceTo() {
		return productsPriceTo;
	}
	public void setProductsPriceTo(int productsPriceTo) {
		this.productsPriceTo = productsPriceTo;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public int getSortDate() {
		return sortDate;
	}
	public void setSortDate(int sortDate) {
		this.sortDate = sortDate;
	}
	
	
	
}
